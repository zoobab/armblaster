static	inline	GPIO_TypeDef *port_p(int pin)
{
	static	GPIO_TypeDef *port_tab[]={
		GPIOA,
		GPIOB,
		GPIOC,
		GPIOD,
		GPIOE,
		GPIOF,
		GPIOG,
		GPIOA,
	};
	return port_tab[(pin >> 4) & 7];
}

