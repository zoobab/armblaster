void	digitalWrite(int pin, int value) 
{
	GPIO_TypeDef *p = port_p(pin);
	int mask = 1 << (pin & 15);
	if(value) {
		p->BSRR = mask;		// bit set pattern
	}else{
		p->BRR  = mask;		// bit reset pattern
	}
}

